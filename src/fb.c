void fb_draw_char(u32 x, u32 y, char c, u32 fg, u32 bg) {

    for(u32 cur_y = 0; cur_y < FONT_HEIGHT; cur_y++) {
        for(u32 cur_x = 0; cur_x < FONT_WIDTH; cur_x++) {
            if ((font8x8_basic[(u8)c][cur_y] >> cur_x) & 1) {
                DRAW_PXL(x + cur_x, y + cur_y, fg);
            } else {
                DRAW_PXL(x + cur_x, y + cur_y, bg);
            }
        }
    }
}


u32 tty_x = 0; 
u32 tty_y = 0;
u32 tty_fg = 0xffffffff;
u32 tty_bg = 0x00000000;


void fb_putc(char c) {

    if (tty_y > FB_RES_Y - FONT_HEIGHT) {
        fb_scroll();
        tty_y -= FONT_HEIGHT;
    }

    if (c != '\n' && c != '\r') fb_draw_char(tty_x, tty_y, c, tty_fg, tty_bg);

    if (c == '\n' || tty_x > FB_RES_X - FONT_WIDTH) {
        tty_x = 0;
        tty_y += FONT_HEIGHT;
    } else tty_x += FONT_WIDTH;
}


void fb_scroll(void) {

    // start dma transfer
    DMA(0)->cb_addr = P2GPU(V2P(&fb_scroll_cb));
    DMA(0)->cs = DMA_CS_WAIT_FOR_OUTSTANDING_WRITES 
        | (DMA_DEFAULT_PANIC_PRIO << DMA_CS_PANIC_PRIO_SHIFT)
        | (DMA_DEFAULT_PRIO << DMA_CS_PRIO_SHIFT)
        | DMA_CS_ACTIVE;

    // wait for it to finish
    while (DMA(0)->cs & DMA_CS_ACTIVE);

    if (DMA(0)->cs & DMA_CS_ERROR) {
        mem_cpy(
            (u8*)fb, 
            (u8*)&fb[FONT_HEIGHT * FB_RES_X], 
            FB_RES_X * (FB_RES_Y - FONT_HEIGHT) * 4);

        for (u32 i = FB_RES_X * (FB_RES_Y - FONT_HEIGHT); i < FB_RES_X * FB_RES_Y; i++) {
            fb[i] = tty_bg;
        }
        dbg_info("FB: DMA error!");
        asm_irq_disable();
        asm_halt();
    }

    // clear last row
    for (u32 i = FB_RES_X * (FB_RES_Y - FONT_HEIGHT); i < FB_RES_X * FB_RES_Y; i++) {
        fb[i] = tty_bg;
    }
}
